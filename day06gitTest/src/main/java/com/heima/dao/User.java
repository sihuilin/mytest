package com.heima.dao;

public class User {
    private Integer id;
    private String name;

    private Integer dsds;
    private String nii;

    public Integer getId() {
        return id;
    }

    public void setId(Integer id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public Integer getDsds() {
        return dsds;
    }

    public void setDsds(Integer dsds) {
        this.dsds = dsds;
    }

    public String getNii() {
        return nii;
    }

    public void setNii(String nii) {
        this.nii = nii;
    }
}
